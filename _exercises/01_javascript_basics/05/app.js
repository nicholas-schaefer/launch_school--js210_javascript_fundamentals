let first_number    = prompt('==> Enter the first number:');
let second_number   = prompt('==> Enter the second number:');


console.log(parseInt(first_number, 10) + parseInt(second_number, 10));
console.log(parseInt(first_number, 10) - parseInt(second_number, 10));
console.log(parseInt(first_number, 10) * parseInt(second_number, 10));
console.log(parseInt(Math.floor(first_number, 10) / parseInt(second_number, 10)));
console.log(parseInt(first_number, 10) % parseInt(second_number, 10));
console.log(parseInt(first_number, 10) ** parseInt(second_number, 10));